#include<unistd.h>

void	print_a(int n)
{
	int a;
	if ( n == -2147483648)
		{
			write(1, "-2147483648", 11);
			return ;
		}
	if( n < 0)
	{
		write(1, "-", 1);
		n *=-1;
	}
	if( n > 0)
	{
		if(!(n/10==0))
			print_a( n /10);
		a = n % 10 + '0';
		write(1, &a, 1);
	}
	if( n == 0 )
	{	
		a = n + '0';
		write(1, &a, 1);
	}
}
			



