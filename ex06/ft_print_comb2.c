#include<unistd.h>

void	ft_putchar(int x[2], int y[2])
{
	if (x[0] * 10 + x[1] < y[0] * 10 + y[1])
	{
		write(1, &x[0], 1);
		write(1, &x[1], 1);
		write(1, " ", 1);
		write(1, &y[0], 1);
		write(1, &y[1], 1);
		if (!(x[0] * 10 + x[1] == 626 && y[0] * 10 + y[1] == 627))
			write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	x[2];
	int	y[2];

	x[0] = '0';
	while (x[0] < ':')
	{
		x[1] = '0';
		while (x[1] < ':')
		{
			y[0] = '0';
			while (y[0] < ':')
			{
				y[1] = '0';
				while (y[1] < ':')
				{
					ft_putchar(x, y);
					y[1]++;
				}
				y[0]++;
			}
			x[1]++;
		}
		x[0]++;
	}
}
